    public void loadMeasurementCategory() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `measurment_type_category` ORDER BY `name` ASC");
            Vector v = new Vector();
            v.add("Select Category");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            jComboBox1.setModel(new DefaultComboBoxModel(v));
        } catch (Exception ex) {
            Logger.getLogger(InventoryRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void loadBrandTable() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = MySQL.search("SELECT * FROM `measurment_type` INNER JOIN measurment_type_category ON measurment_type.measurment_type_category_id=measurment_type_category.id ORDER BY measurment_type.name ASC");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("measurment_type_category.name"));
                v.add(rs.getString("measurment_type.name"));
                v.add(rs.getString("containing_amount"));
                dtm.addRow(v);
            }
            jTable1.setModel(dtm);
        } catch (Exception ex) {
            Logger.getLogger(MeasurementTypeRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

