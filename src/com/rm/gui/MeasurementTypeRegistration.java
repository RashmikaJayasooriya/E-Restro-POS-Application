    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Measurment Type Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Measurement Type Containing Amount In Gram or Mililiter", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please Select Measurement Type Category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            if (jButton1.getText().equals("Save")) {
                try {
                    ResultSet check = MySQL.search("SELECT * FROM `measurment_type` WHERE `name`='" + jTextField1.getText() + "' AND `containing_amount`='" + jTextField2.getText() + "' ");
                    if (check.next()) {
                        JOptionPane.showMessageDialog(this, "Measurment Type Already Registered", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        ResultSet rmtc = MySQL.search("SELECT * FROM `measurment_type_category` WHERE `name`='" + jComboBox1.getSelectedItem() + "'");
                        rmtc.next();
                        MySQL.iud("INSERT INTO `measurment_type`(`name`,`containing_amount`,measurment_type_category_id) VALUES('" + jTextField1.getText() + "','" + jTextField2.getText() + "','" + rmtc.getString("id") + "')");
                        resetFields();
                        JOptionPane.showMessageDialog(this, "Measurment Type Added SuccessFully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(MeasurementTypeRegistration.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    ResultSet check = MySQL.search("SELECT * FROM `measurment_type` WHERE `name`='" + jTextField1.getText() + "' AND `containing_amount`='" + jTextField2.getText() + "' AND `id`<>'" + jLabel3.getText() + "' ");
                    if (check.next()) {
                        JOptionPane.showMessageDialog(this, "Measurment Type Already Registered", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        MySQL.iud("UPDATE `measurment_type` SET `name`='" + jTextField1.getText() + "', `containing_amount`='" + jTextField2.getText() + "' WHERE `id`='" + jLabel3.getText() + "'");
                        resetFields();
                        JOptionPane.showMessageDialog(this, "Measurment Type Updated SuccessFully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(MeasurementTypeRegistration.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            if (rf != null) {
                rf.loadMeasurment();
            } else if (po != null) {
                po.loadMeasurmentType();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int sr = jTable1.getSelectedRow();
        if (evt.getClickCount() == 3) {
            if (SignIn.user_type_id == 1 || SignIn.user_type_id == 2 || SignIn.user_type_id == 3) {
                try {
                    ResultSet scheck = MySQL.search("SELECT * FROM `stock` WHERE `measurment_type_id`='" + jTable1.getValueAt(sr, 0) + "' ");
                    ResultSet pcheck = MySQL.search("SELECT * FROM `purchase_order_item` WHERE `measurment_type_id`='" + jTable1.getValueAt(sr, 0) + "' ");
                    ResultSet mcheck = MySQL.search("SELECT * FROM `meal_item_has_inventory_item` WHERE `measurment_type_id`='" + jTable1.getValueAt(sr, 0) + "' ");
                    ResultSet gcheck = MySQL.search("SELECT * FROM `grn_item` WHERE `measurment_type`='" + jTable1.getValueAt(sr, 0) + "' ");
                    if (scheck.next()) {
                        JOptionPane.showMessageDialog(this, "Stock Items Has Been Registered Under This Measurement Type", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else if (pcheck.next()) {
                        JOptionPane.showMessageDialog(this, "Purchase Orders Has Been Registered Under This Measurement Type", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else if (mcheck.next()) {
                        JOptionPane.showMessageDialog(this, "Meal Items Has Been Registered Under This Measurement Type", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else if (gcheck.next()) {
                        JOptionPane.showMessageDialog(this, "GRN Items Has Been Registered Under This Measurement Type", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        MySQL.iud("DELETE FROM `measurment_type` WHERE `id`='" + jTable1.getValueAt(sr, 0) + "'");
                        loadBrandTable();
                        JOptionPane.showMessageDialog(this, "Measurment Type Deleted Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                        resetFields();
                    }
                } catch (Exception ex) {
                    Logger.getLogger(MeasurementTypeRegistration.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Only Admin or A General Manager or Assistant Manager Can Delete a Measurment Type", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } else if (evt.getClickCount() == 2) {
            jTextField1.setText(jTable1.getValueAt(sr, 2).toString());
            jTextField2.setText(jTable1.getValueAt(sr, 3).toString());
            jComboBox1.setSelectedItem(jTable1.getValueAt(sr, 1));
            jComboBox1.setEnabled(false);
            jLabel3.setText(jTable1.getValueAt(sr, 0).toString());
            jLabel4.setText("Id :");
            jButton1.setText("Update");
        }
    }//GEN-LAST:event_jTable1MouseClicked

